#4 funciones basicas en python (suma,resta, multiplicacion y division)
def add(num1,num2):
    return num1 + num2

def substract(num1,num2):
    return num1 - num2

def multiply(num1,num2):
    return num1 * num2

def divide(num1,num2):
    return num1 / num2

print(add(4,9))
print(substract(3,7))
print(multiply(9,-23))
print(divide(12,3))
